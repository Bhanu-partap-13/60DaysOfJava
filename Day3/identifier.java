package Day3;
//Identifier can be a class name,  variable name, method name, package name, constant name.

public class identifier { // class name
    public static void main(String[] args){ // method name(var. name)
        int x = 100; //var name
        System.out.println("x: "+x);
    }
}

// ---------------------------------------------------
// Rules for Identifiers in Java:
// 1. Can contain letters (A-Z, a-z), digits (0-9), underscore (_) and dollar sign ($).
// 2. Cannot start with a digit (e.g., 1variable ‚ùå is invalid).
// 3. Cannot be a keyword (e.g., "int", "class", "public" are not allowed).
// 4. Java is case-sensitive (e.g., "MyVar" and "myVar" are different).
// 5. Can be of any length but should be meaningful.
// 6. Should follow camelCase for variable names (e.g., "myVariable").
// ---------------------------------------------------
